import 'package:flutter/material.dart';

class AppWidget {
  static TextStyle whiteBoldTextStyle(double size) {
    return TextStyle(
      color: Colors.white,
      fontFamily: "Poppins",
      fontSize: size,
    );
  }

  static TextStyle blackBoldTextStyle(double size) {
    return TextStyle(
      color: Colors.black,
      fontFamily: "Poppins",
      fontSize: size,
    );
  }
}
